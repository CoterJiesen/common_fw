func transformTestCards(in []int32) (out []*common.CardInformation) {
	for _, v := range in {
		out = append(out, &common.CardInformation{
			Type:  common.CardType(v / 10).Enum(),
			Value: proto.Uint32(uint32(v % 10)),
		})
	}
	return
}

func testMjAna(cardsTest MJCardsTest, t *testing.T) (pass bool) {
	t.Logf("TestCase=%v", cardsTest)
	testCards := transformTestCards(cardsTest.Cards)

	t.Logf("testCards=%v", testCards)
	AnaRes := Split(testCards)
	for _, v := range AnaRes {
		t.Logf("AnaRes=%v", v)
	}
	for _, v := range AnaRes {
		if v != nil {
			if compareAnaResult(v, cardsTest.Expect, t) {
				pass = true
				break
			}
		}
	}
	return
}

func compareExAx(ex []int32, ax []*common.CardInformation, t *testing.T) (equal bool) {
	t.Logf("compareExAx,exl=%v,ex=%v", len(ex), ex)
	t.Logf("compareExAx,axl=%v,ax=%v", len(ax), ax)
	if len(ex) == len(ax) {
		for i := 0; i < len(ex); i++ {
			ex := ex[i]
			ax := ax[i]
			if common.CardType(ex/10) != ax.GetType() || uint32(ex%10) != ax.GetValue() {
				return
			}
		}
	} else {
		return
	}

	equal = true
	return
}

func compareAnaResult(ar *common.AnalyzeResult, er MJAnaResultTest, t *testing.T) (equal bool) {
	sort.Sort(Cards4Sort(ar.Left))
	sort.Sort(TestCards4Sort(er.Left))
	if !compareExAx(er.Left, ar.Left, t) {
		return
	}
	sort.Sort(Cards4Sort(ar.Seqs))
	sort.Sort(TestCards4Sort(er.Seqs))
	if !compareExAx(er.Seqs, ar.Seqs, t) {
		return
	}
	sort.Sort(Cards4Sort(ar.Threes))
	sort.Sort(TestCards4Sort(er.Threes))
	if !compareExAx(er.Threes, ar.Threes, t) {
		return
	}
	sort.Sort(Cards4Sort(ar.Twos))
	sort.Sort(TestCards4Sort(er.Twos))
	if !compareExAx(er.Twos, ar.Twos, t) {
		return
	}
	equal = true
	return
}

