func testPreWildAna(data PreAnaWildCardsTest, t *testing.T) (pass bool) {
	testCards := transformTestCards(data.Cards)
	testWild := transformTestCards([]int32{data.Wild})
	noWildCards, WildNum := excludCard(testCards, testWild[0])
	menAnaTest := PreAna3NP2WildCards(noWildCards, testWild[0], WildNum)
	t.Logf("menAnaTest=%v", menAnaTest)
	for i := 0; i < len(menAnaTest); i++ {
		if !compareMenAna(menAnaTest[i], data.Expect[i]) {
			return
		}
	}
	pass = true
	return
}
func compareMenAna(ar []int32, er []int32) (equal bool) {
	if len(ar) != len(er) {
		return
	}

	for i := 0; i < len(ar); i++ {
		if ar[i] != er[i] {
			return
		}
	}
	equal = true
	return
}

