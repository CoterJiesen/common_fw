	testWild := transformTestCards([]int32{testData.Wild})
	noWildCards, WildNum := excludCard(testCards, testWild[0])

	menAnaTest := PreAna3NP2WildCards(noWildCards, testWild[0], WildNum)
	AnaOut := Ana3NP2WildMenCards(noWildCards, testWild[0], WildNum, menAnaTest)

